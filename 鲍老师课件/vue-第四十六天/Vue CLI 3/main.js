

// Vue cli3

// 是基于 webpack 构建的，并且带有相关合理的 webpack配置，为了开发者能够 基于 Vue进行 快速项目开发

// 使用 Vue cli3
// 1.安装 vue cl3：npm install @vue/cli -g
// 2.创建一个 初始化的 Vue项目：使用 vue create 项目名称 

// vue create生成的项目结构
// 1.dist：通过 npm run build 生成最终可以直接运行的代码
// 2.node_modules：通过 npm install 安装的所有项目依赖和开发依赖的模块
// 3.public：项目的 公共资源，它里面的所有文件都会被直接打包到 最终可以运行的目录中
// 4.src：整个项目的 源代码文件
// 5.assets：项目所有的静态资源（font、css、img）
// 6.components：全局组件
// 7.App.vue：根组件
// main.js：Vue项目的 入口模块文件

// .gitignore：git仓库进行代码提交时忽略文件的信息
// babel.config.js：babel转换器（es6代码转换成es5）的配置文件
// package.json：项目开发信息
// package-lock.json：运行 npm install时自动生成的，用来记录当前项目中 实际安装的各个模块的具体来源和版本号
// README.md：markdown文件

